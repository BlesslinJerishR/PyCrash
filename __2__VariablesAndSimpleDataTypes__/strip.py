word = "    python    "
print(word)
print(f"length : {len(word)}")

#rstrip
word_rstrip = word.rstrip()
print(word_rstrip)
print(f"length : {len(word_rstrip)}")

#lstrip
word_lstrip = word.lstrip()
print(word_lstrip)
print(f"length : {len(word_lstrip)}")

#strip
word_strip = word.strip()
print(word_strip)
print(f"length : {len(word_strip)}")